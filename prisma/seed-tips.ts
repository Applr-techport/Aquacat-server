async function main() {
  const existing = await prisma.healthTip.count();
  if (existing > 0) {
    console.log(`이미 ${existing}개 팁이 있어요. 스킵!`);
    return;
  }
  for (const content of tips) {
    await prisma.healthTip.create({ data: { content } });
  }
  console.log(`✅ ${tips.length}개 건강 팁 시드 완료!`);
}

main().finally(() => prisma.$disconnect());
